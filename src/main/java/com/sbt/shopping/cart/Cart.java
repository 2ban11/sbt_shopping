package com.sbt.shopping.cart;

public class Cart {
	private int c_no;
	private String c_id;
	private int c_product;
	private int c_cnt;
	public int getC_no() {
		return c_no;
	}
	public void setC_no(int c_no) {
		this.c_no = c_no;
	}
	public String getC_id() {
		return c_id;
	}
	public void setC_id(String c_id) {
		this.c_id = c_id;
	}
	public int getC_product() {
		return c_product;
	}
	public void setC_product(int c_product) {
		this.c_product = c_product;
	}
	public int getC_cnt() {
		return c_cnt;
	}
	public void setC_cnt(int c_cnt) {
		this.c_cnt = c_cnt;
	}
	
	
}
